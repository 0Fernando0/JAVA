package aula_7;

public class Aula_7{
    public static void main(String[] args) {
        // LAÇO FOR
        for(int c = 1; c <= 10 ; c++){
            System.out.println(c);
        }

        // LAÇO WHILE
        int l = 1;
        while(l <= 10){
            System.out.println(l);
            l++;
        }

        // LAÇO DO WHILE
        int i = 1;
        do{
            System.out.println(i);
            i++;
        }while(i <= 10);
    }
}